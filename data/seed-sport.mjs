function mergeLevel(lvl) {
  const base = load(`sport-${lvl}.json`).map(([q, a, w]) => rewrite(q, a, w));
  const sup = SUPPLEMENT[lvl] || [];
  let pool = dedupe([...base, ...sup]);
  pool = pool.filter(([q]) => !/— вид спорта\?$/i.test(q) && !/^«[^»]+» — вид/i.test(q));
  return pool;
}

for (const lvl of ['easy', 'medium', 'hard']) {
  const pool = mergeLevel(lvl);
  fs.writeFileSync(path.join(__dirname, `sport-${lvl}.json`), JSON.stringify(pool, null, 2));
  console.log(`sport-${lvl}.json: ${pool.length}`);
}
