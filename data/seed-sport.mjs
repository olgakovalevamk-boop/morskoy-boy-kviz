function load(name) {
  const p = path.join(__dirname, name);
  return fs.existsSync(p) ? JSON.parse(fs.readFileSync(p, 'utf8')) : [];
}

function rewrite(q, a, w) {
  let text = q;
  text = text.replace(/«Уимблдон» — вид спорта\?/, 'Турнир Уимблдон — это соревнования по');
  text = text.replace(/«Супербоул» — вид спорта\?/, 'Финал «Супербоул» относится к');
  text = text.replace(/«Тур де Франс» — вид спорта\?/, '«Тур де Франс» — знаменитая');
  text = text.replace(/«Роланд Гаррос» — покрытие корта\?/, 'На «Ролан Гаррос» корты покрыты');
  text = text.replace(/Кубок УЕФА — вид спорта\?/, 'Кубок УЕФА разыгрывают в');
  text = text.replace(/Серия «Мастерс» — вид спорта\?/, 'Серия «Мастерс» — турниры по');
  text = text.replace(/— вид спорта\?$/i, ' — это соревнования в');
  if (text.endsWith('...')) text = text.replace(/\.\.\.$/, '?');
  return [text, a, w];
}

function dedupe(arr) {
  const s = new Set();
  return arr.filter(([q]) => (s.has(q) ? false : (s.add(q), true)));
}

